"""
Mathematical operations.
"""


def add_numbers(
    first_number: int,
    second_number: int
) -> int:
    """
    Return addition.
    """

    return (
        first_number +
        second_number
    )


def subtract_numbers(
    first_number: int,
    second_number: int
) -> int:
    """
    Return subtraction.
    """

    return (
        first_number -
        second_number
    )


def multiply_numbers(
    first_number: int,
    second_number: int
) -> int:
    """
    Return multiplication.
    """

    return (
        first_number *
        second_number
    )


def divide_numbers(
    first_number: float,
    second_number: float
) -> float:
    """
    Return division.
    """

    return (
        first_number /
        second_number
    )